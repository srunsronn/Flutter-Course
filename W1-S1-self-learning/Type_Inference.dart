// Explain how Dart infers the type of a variable.
// Dart infers the type of a variable based on the type of the value assigned to it.

void main() {
  // Declare a int variable and let Dart infer its type
  var age = 20;
  var location = "Takeo province";
  var price = 19.99;

  // Define a variable with an explicit String type
  int year = 2004;
  String name = "Leonel Messi";

  // print the output
  print("===============");
  print(age);
  print("===============");
  print(location);
  print("===============");
  print(price);
  print("===============");
  print(year);
  print("===============");
  print(name);
  print("===============");
}
